import UIKit

extension UIColor {
    static func accentOrange() -> UIColor {
        return #colorLiteral(red: 0.8679435849, green: 0.4131185412, blue: 0, alpha: 1)
    }
    
    static func gray() -> UIColor {
        return #colorLiteral(red: 0.9254901961, green: 0.9411764706, blue: 0.9450980392, alpha: 1)
    }
    
    static func backgroundColor() -> UIColor {
        return #colorLiteral(red: 0.8980392157, green: 0.8980392157, blue: 0.8980392157, alpha: 1)
    }
    
    static func textfieldColor() -> UIColor {
        return #colorLiteral(red: 0.9267373085, green: 0.9416674972, blue: 0.9457096457, alpha: 1)
    }
}

