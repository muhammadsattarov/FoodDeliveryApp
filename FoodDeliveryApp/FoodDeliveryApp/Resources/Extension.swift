import UIKit

extension UIColor {
    static func selectedColor() -> UIColor {
        return #colorLiteral(red: 0.8679435849, green: 0.4131185412, blue: 0, alpha: 1)
    }
    
    static func tabbarIconColor() -> UIColor {
        return #colorLiteral(red: 0.501960814, green: 0.501960814, blue: 0.501960814, alpha: 1)
    }
    
    static func backgroundColor() -> UIColor {
        return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
    }
    
    static func textfieldColor() -> UIColor {
        return #colorLiteral(red: 0.9267373085, green: 0.9416674972, blue: 0.9457096457, alpha: 1)
    }
}
